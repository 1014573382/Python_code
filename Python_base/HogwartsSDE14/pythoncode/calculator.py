# 实现计算器

class Calculator():
    def add(self, a, b):
        return a + b

    def add1(self, a, b):
        return a + b

    def add2(self, a, b):
        return a + b

    def div(self, a, b):
        return a / b

    def sub(self, a, b):
        return a - b

